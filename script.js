// Add active class to current page in navigation
document.addEventListener('DOMContentLoaded', function() {
    const currentLocation = window.location.pathname;
    const navLinks = document.querySelectorAll('.nav-menu a');
    
    navLinks.forEach(link => {
        const linkPath = new URL(link.href).pathname;
        if (linkPath === currentLocation || 
            (currentLocation.endsWith('/') && linkPath === currentLocation + 'index.html') ||
            (linkPath.endsWith('index.html') && currentLocation === linkPath.replace('index.html', ''))) {
            link.classList.add('active');
        }
    });
});

// Form validation (if needed for future custom forms)
function validateForm(formElement) {
    const inputs = formElement.querySelectorAll('input[required], textarea[required]');
    let isValid = true;
    
    inputs.forEach(input => {
        if (!input.value.trim()) {
            isValid = false;
            input.style.borderColor = '#ff4444';
        } else {
            input.style.borderColor = '#d4d4d4';
        }
    });
    
    return isValid;
}

